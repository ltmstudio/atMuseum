(function(){
  const box   = document.getElementById('poem-scroll');
  const start = document.getElementById('autoStart');
  const stop  = document.getElementById('autoStop');
  const topBtn= document.getElementById('toTop');

  if (!box) return; // защита, если элемент не найден

  let rafId = null;
  let speed = 0.35; // px / кадр (~21px/сек при 60fps)

  function step(){
    if (Math.ceil(box.scrollTop + box.clientHeight) >= box.scrollHeight) {
      cancelAnim();
      return;
    }
    box.scrollTop += speed;
    rafId = requestAnimationFrame(step);
  }

  function startAnim(){
    cancelAnim();
    rafId = requestAnimationFrame(step);
  }

  function cancelAnim(){
    if (rafId) cancelAnimationFrame(rafId);
    rafId = null;
  }

  // управление кнопками
  start?.addEventListener('click', startAnim);
  stop?.addEventListener('click', cancelAnim);
  topBtn?.addEventListener('click', () => {
    cancelAnim();
    box.scrollTo({ top: 0, behavior: 'smooth' });
  });

  // пауза при ручном взаимодействии
  ['pointerdown','wheel','touchstart','mouseenter']
    .forEach(ev => box.addEventListener(ev, cancelAnim, { passive: true }));
})();



// ====== Скролл стихотворения: автоскролл + drag-to-scroll ======
(function(){
    const box   = document.getElementById('poem-scroll');
    if (!box) return;
  
    // --- Автоскролл ---
    let rafId = null;
    let speed = 0; // px/кадр (~60px/сек при 60fps) — скорость увеличена для более быстрого скролла
  
    function step(){
      // если дошли до низа — стоп
      if (Math.ceil(box.scrollTop + box.clientHeight) >= box.scrollHeight) {
        cancelAnim(); return;
      }
      box.scrollTop += speed;
      rafId = requestAnimationFrame(step);
    }
    function startAnim(){ cancelAnim(); rafId = requestAnimationFrame(step); }
    function cancelAnim(){ if (rafId) cancelAnimationFrame(rafId); rafId = null; }
  
    // (опционально) Экспортируем в window кнопки, если есть в верстке
    const btnStart = document.getElementById('autoStart');
    const btnStop  = document.getElementById('autoStop');
    const btnTop   = document.getElementById('toTop');
  
    btnStart?.addEventListener('click', startAnim);
    btnStop ?.addEventListener('click', cancelAnim);
    btnTop  ?.addEventListener('click', ()=>{ cancelAnim(); box.scrollTo({top:0, behavior:'smooth'}); });
  
    // --- Пауза автоскролла при любом ручном действии ---
    ['pointerdown','wheel','touchstart','keydown','mouseenter']
      .forEach(ev => box.addEventListener(ev, cancelAnim, {passive:true}));
  
    // --- Drag-to-scroll для мыши/перо (Pointer Events) ---
    let isDown = false;
    let startY = 0;
    let startTop = 0;
  
    box.addEventListener('pointerdown', (e) => {
      // работаем только для мыши/пера; для тача и так нативный скролл
      if (e.pointerType === 'mouse' || e.pointerType === 'pen') {
        isDown = true;
        startY = e.clientY;
        startTop = box.scrollTop;
        box.classList.add('dragging');
        cancelAnim();
        // фиксируем указатель, чтобы получать move за пределами
        box.setPointerCapture?.(e.pointerId);
        e.preventDefault();
      }
    });
  
    box.addEventListener('pointermove', (e) => {
      if (!isDown) return;
      const dy = e.clientY - startY;
      box.scrollTop = startTop - dy; // интуитивное направление
      e.preventDefault();
    });
  
    function endDrag(e){
      if (!isDown) return;
      isDown = false;
      box.classList.remove('dragging');
      box.releasePointerCapture?.(e.pointerId);
    }
    box.addEventListener('pointerup', endDrag);
    box.addEventListener('pointercancel', endDrag);
    box.addEventListener('pointerleave', endDrag);
  
   
    setTimeout(() => startAnim(), 10);
  })();
  